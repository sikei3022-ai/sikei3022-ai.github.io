  '',
  'БЕГ (Natural Running — Д.Эбшир/Б.Метцлер; учебники по лёгкой атлетике):',
  '- Приземляйся под центр тяжести, не вынося ногу далеко вперёд — это гасит удар и бережёт колени.',
  '- Контакт средней частью стопы, а не жёстко пяткой; ставь стопу мягко, «под себя».',
  '- Держи высокий каденс — около 170-180 шагов/мин; следующий шаг начинай сразу после касания.',
  '- Корпус слегка наклонён вперёд от голеностопа (не в пояснице), осанка прямая, плечи расслаблены.',
